package com.timebank.controller.yl;

public class UpdateList {
    private int updateId;
    private int deletaId;
    private int startId;
    private int viewVolId;
    private int finishId;
    private int unFinishId;

    public int getUpdateId() {
        return updateId;
    }

    public void setUpdateId(int updateId) {
        this.updateId = updateId;
    }

    public int getDeletaId() {
        return deletaId;
    }

    public void setDeletaId(int deletaId) {
        this.deletaId = deletaId;
    }

    public int getStartId() {
        return startId;
    }

    public void setStartId(int startId) {
        this.startId = startId;
    }

    public int getViewVolId() {
        return viewVolId;
    }

    public void setViewVolId(int viewVolId) {
        this.viewVolId = viewVolId;
    }

    public int getFinishId() {
        return finishId;
    }

    public void setFinishId(int finishId) {
        this.finishId = finishId;
    }

    public int getUnFinishId() {
        return unFinishId;
    }

    public void setUnFinishId(int unFinishId) {
        this.unFinishId = unFinishId;
    }
}
